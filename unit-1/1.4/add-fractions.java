public class AddFractions 
{
    public static void main(String[] args)
    {
        
        int numerator1 = 10;
        int denominator1 = 2;
        int numerator2 = 15;
        int denominator2 = 5;
        
        System.out.println("The numerator of the first fraction is " + 10);
        System.out.println("The denominator of the first fraction is "  + 2);
        System.out.println("The numerator of the first fraction is "  + 15);
        System.out.println("The denominator of the first fraction is "  + 5);
        System.out.print("The sum of ");
        System.out.print((numerator1 + "/" + denominator1) + " + ");
        System.out.print((numerator2 + "/" + denominator2) + " = ");
        System.out.print((numerator1 + "/" + denominator1) + (numerator2 + "/" + denominator2));
        
    }
}
